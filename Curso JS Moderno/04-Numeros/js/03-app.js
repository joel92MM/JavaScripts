/* redondear */
resultado=Math.round(2.5);

/* redondear hacia arriba */
resultado=Math.ceil(2.5);
console.log(resultado);
/* redondear hacia abajo */
resultado=Math.floor(2.9);
console.log(resultado);
/* raiz cuadrada */
resultado=Math.sqrt(55);
console.log(resultado);
/* absoluto */
resultado=Math.abs(500);
console.log(resultado);

/* potencia */
resultado=Math.pow(5,4);
console.log(resultado);

/* maximo */
resultado=Math.max(1,4,6,2);
console.log(resultado);

/* minimo */
resultado=Math.min(1,4,6,2);
console.log(resultado);

/* aleatorio */
resultado=Math.random()+20;
